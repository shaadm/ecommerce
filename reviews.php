<h2>2023 BMW 4-Series Gran Coupe</p>
</h2>
<h2>Overview</h2>
<p>Forget that the 2023 BMW 4-series Gran Coupe has “coupe” in its name; think of it as 4-a four-door sports sedan in
    two-door drag. Like the regular 3-series sedan and 4-series coupe, the 4-series Gran Coupe offers a polished and
    well-ordered cabin and your choice of a turbocharged four- or turbocharged six-cylinder engine. The latter, called
    M440i, hustled to 60 mph in just 3.9 seconds at our test track and feels capable enough on a twisty road to have you
    thinking “M3.”. Headroom in the back seat isn’t as generous as in the more upright 3-series sedans, but the
    4-series’ fastback design cleverly hides a large liftback-style rear door. The Audi A5 Sportback and the Kia Stinger
    also use this same trick, but the BMW’s road manners are more refined than those two rivals’. If you like the
    4-series’ looks but want something more eco-friendly, consider the i4, which shares the 4-series Gran Coupe’s body
    and interior but substitutes an all-electric powertrain.</p>

<h2>What's New for 2023?</h2>
<p>BMW has made all-wheel drive an option on the base 430i Gran Coupe this year. Likewise, the more powerful M440i,
    which was offered only with all-wheel drive last year, now can be had in rear-wheel drive form. Not much else has
    changed, although the optional Shadowline package now adds M Sport Brakes with painted calipers, and the Premium
    package no longer includes gesture controls for the car’s infotainment system. All models now come with a
    faux-leather dashboard over; a Parking Assistance package has been added to the options sheet; and a head-up display
    is now only available on the M440i.</p>
<h2>Pricing and Which One to Buy</h2>
<p><b>430i - $46,895 and the M440i - $58,445</p></b>The price to upgrade to the more powerful M440i model is a lot, but
luckily the base 430i offers plenty to like. It comes standard with a power front seats, a sunroof, in-dash navigation,
dual-zone automatic climate control, parking sensors, a 12.3-inch infotainment display, and a host of driver-assistance
features, among other niceties. We'd suggest the Premium package too, which adds heated front seats, a heated steering
wheel,
and ambient interior lighting.
<h2>Engine, Transmission, and Performance</h2>
<p>Two turbocharged powertrains are on offer here,
    both of them excellent. The base engine is a 2.0-liter four-cylinder in
    the 430i model that makes 255 horsepower. The more powerful upgrade engine
    is a stonking 382-hp 3.0-liter inline-six that’s augmented by a 48-volt
    hybrid system. Both engines come standard with an eight-speed automatic
    and rear-wheel drive; all-wheel drive is optional. We haven’t tested a
    430i Gran Coupe model, but a M440i ripped to 60 mph in just 3.9 seconds
    at our test track. The suspension can feel bit stiff over big potholes,
    but the tradeoff is athletic handling that provides grin-producing entertainment
    on a twisty road.</p>
<h2>Fuel Economy and Real-World MPG</h2>
<p>The EPA estimates that the 430i is good for 25 mpg city and 34 mpg highway,
    while the more powerful M440i is rated for 24 mpg city and 32 mpg highway.
    We haven't had a chance to test either car on our 75-mph highway fuel economy route,
    but when we do we'll update this story with details. For more information about
    the 4-series Gran Coupe's fuel economy, visit the EPA's website.</p>
<h2>Interior, Comfort, and Cargo</h2>
<p>Cabin styling closely resembles that of the 4-series coupe and convertible,
    with a driver-focused dashboard that angles the climate controls and infotainment
    display slightly toward the left. Materials are top-notch and the interior looks
    quite premium, but rival sedans such as the new Mercedes-Benz C-class offer more
    dramatic interior designs. The rear seat is roomier here than on the two-door
    4-series model, but headroom is snug compared to the 3-series sedan because of
    the Gran Coupe's fastback roofline. The Gran Coupe's trunk should offer far more
    space for luggage, groceries, and other cargo than the two-door models, too, but
    we won't know exactly how much more room until we get one in for testing.</p>
<h2>Infotainment and Connectivity</h2>
<p>All models come with a large 10.3-inch infotainment display as well as
    a 12.3-inch digital gauge display. Apple CarPlay and Android Auto are both
    standard and work wirelessly; in-dash navigation, SiriusXM satellite radio,
    and a Wi-Fit hotspot are also included. The standard stereo is a 10-speaker
    system but a 16-speaker Harman/Kardon stereo is optional.</p>


